"""
PAG1 HADDOCK3 Refinement + Multi-Structure ΔΔG Pipeline
========================================================
Full workflow:
  1. Split AF2-Multimer PDB into antibody + antigen
  2. Generate HADDOCK3 AIRs from CDR→antigen interface contacts
  3. Run HADDOCK3 flexible refinement (WSL)
  4. Collect top-N refined structures
  5. Run EvoEF2 + MM/GBSA ΔΔG scan on multiple structures
  6. Compare AF2 vs HADDOCK results → consensus ranking

Requirements:
  - HADDOCK3 installed in WSL (Ubuntu-22.04)
  - EvoEF2, OpenMM in affmat conda env (Windows)
"""

