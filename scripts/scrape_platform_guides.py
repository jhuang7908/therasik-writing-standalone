# ── Scraper ────────────────────────────────────────────────────────────────────

async def scrape_platform(page: Page, system_key: str, target: dict) -> dict:
    """Attempt to scrape live documentation, fall back to built-in content."""
    scraped_text = ""

    for url in target.get("urls", []):
        try:
            await page.goto(url, timeout=20000, wait_until="domcontentloaded")
            await page.wait_for_timeout(2000)

            # Dismiss cookie banners
            for sel in ["button:has-text('Accept')", "button:has-text('Accept all')",
                        "#onetrust-accept-btn-handler", ".cookie-accept"]:
                try:
                    btn = page.locator(sel).first
                    if await btn.is_visible():
                        await btn.click()
                        await page.wait_for_timeout(500)
                except Exception:
                    pass

            text = await page.evaluate("() => document.body.innerText")
            if len(text) > 500:
                scraped_text += "\n\n" + text[:8000]
                break
        except Exception:
            continue

    # Build output: start with fallback, enhance with scraped text if available
    content = dict(target.get("fallback_content", {}))
    content["system_key"]   = system_key
    content["scraped_text"] = scraped_text[:4000] if scraped_text else ""
    content["scraped_at"]   = datetime.now(timezone.utc).isoformat()
    content["source_urls"]  = target.get("urls", [])

    # Extract additional details from scraped text
    if scraped_text:
        # Try to find file size limits mentioned on the page
        size_m = re.search(r"(\d+)\s*MB\s+(?:file\s+size|maximum|limit|per\s+file)",
                           scraped_text, re.IGNORECASE)
        if size_m:
            content.setdefault("file_requirements", {})["max_file_size_mb"] = int(size_m.group(1))

    return content


async def run(
    output_dir: Path,
    systems: list[str] | None = None,
    headless: bool = True,
    dry_run: bool = False,
) -> dict:
    output_dir.mkdir(parents=True, exist_ok=True)
    targets = {k: v for k, v in PLATFORM_TARGETS.items()
               if not systems or k in systems}

    results = {}
    async with async_playwright() as pw:
        browser = await pw.chromium.launch(headless=headless)
        ctx     = await browser.new_context(
            user_agent=(
                "Mozilla/5.0 (Windows NT 10.0; Win64; x64) "
                "AppleWebKit/537.36 (KHTML, like Gecko) "
                "Chrome/120.0.0.0 Safari/537.36"
            )
        )
        page = await ctx.new_page()

        for key, target in targets.items():
            print(f"  {key} ...", end=" ", flush=True)
            content = await scrape_platform(page, key, target)
            results[key] = content

            if not dry_run:
                out_file = output_dir / f"{key.lower().replace(' ', '_')}.json"
                out_file.write_text(
                    json.dumps(content, indent=2, ensure_ascii=False), encoding="utf-8"
                )
                print(f"✓ → {out_file.name}")
            else:
                print("✓ (dry run)")

        await browser.close()

    # Write index
    if not dry_run:
        index = {
            k: {
                "key":         k,
                "description": v.get("description", "")[:120],
                "source_urls": v.get("source_urls", []),
                "scraped_at":  v.get("scraped_at", ""),
            }
            for k, v in results.items()
        }
        (output_dir / "_index.json").write_text(
            json.dumps(index, indent=2, ensure_ascii=False), encoding="utf-8"
        )
        print(f"\n  Index written: {output_dir / '_index.json'}")

    return results


def main():
    parser = argparse.ArgumentParser(description="Submission platform guide scraper")
    parser.add_argument("--system",    nargs="+", help="Specific system keys to scrape")
    parser.add_argument("--dry-run",   action="store_true")
    parser.add_argument("--no-headless", action="store_true")
    parser.add_argument("--output-dir", default=str(OUTPUT_DIR))
    args = parser.parse_args()

    print(f"Scraping submission platform guides → {args.output_dir}")
    if args.dry_run:
        print("  DRY RUN")
    print()

    results = asyncio.run(run(
        Path(args.output_dir),
        systems=args.system,
        headless=not args.no_headless,
        dry_run=args.dry_run,
    ))

    print(f"\nDone: {len(results)} platform guides written.")


if __name__ == "__main__":
    main()
