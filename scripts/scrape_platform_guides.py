"""
scrape_platform_guides.py
==========================
Scrapes submission platform (system-level) documentation pages using Playwright.

Each major submission system publishes platform-wide author guides covering:
  - File format requirements (across ALL journals on the platform)
  - Upload order and step-by-step submission flow
  - Account setup requirements
  - Common errors and how to fix them

Output: assets/submission_system_guides/{system_key}.json

Usage:
  python scripts/scrape_platform_guides.py [--system ScholarOne] [--dry-run]

  --system S   Only scrape one system (key from PLATFORM_TARGETS)
  --dry-run    Print extracted content without writing files
  --no-headless  Show browser window
"""

