# ── 2. INTRODUCTION  (5-paragraph funnel, clean transitions) ──────────────
INTRODUCTION = """\
Innate immune cells are the first responders to infection and tissue injury, detecting conserved molecular patterns through dedicated receptor families and initiating downstream signaling cascades that shape both local and systemic inflammation. Among the best-characterized of these cascades are NF-κB-dependent transcription, which drives pro-inflammatory cytokine gene expression; type I interferon signaling, which coordinates antiviral and immunomodulatory programs; and NLRP3 inflammasome activation, which mediates post-translational processing of IL-1β and IL-18 (Swanson et al., 2019). Although each pathway is individually essential for protective immunity, their dysregulation underlies severe autoinflammatory syndromes, sepsis, chronic inflammatory bowel disease, and a growing list of complex multifactorial disorders (Dinarello, 2018).

Despite extensive mechanistic work in conventional mouse models, translating preclinical innate immunity research into effective human therapies has proven difficult. The innate immune system exhibits striking species differences: human and murine toll-like receptor repertoires, cytokine networks, and inflammasome compositions diverge at multiple levels, meaning that conclusions drawn from mouse models do not always predict human biology or clinical drug responses (Seok et al., 2013). Studying human innate immune pathways in controlled in vivo settings therefore requires dedicated experimental systems that cannot be replicated using standard inbred strains.

Humanized mouse models, generated by engrafting human hematopoietic stem and progenitor cells (HSPCs) into immunodeficient hosts, provide one solution. NOD-scid-Il2rγ−/− (NSG) mice support efficient HSPC engraftment and have enabled substantial advances in human adaptive immunology; however, the absence of appropriate cross-reactive growth factor signaling in standard NSG mice results in poor development of human monocytes, dendritic cells, and granulocytes—the key effectors of innate immunity (Shultz et al., 2012). This myeloid deficiency severely limits the utility of conventional NSG-based platforms for innate immunity research.

Targeted transgenic expression of human myeloid growth factors can rescue this limitation. NSG-SGM3 mice, expressing human SCF, GM-CSF, and IL-3, markedly improve granulocyte and monocyte engraftment, while NSG-CSF1 mice, expressing human CSF1, selectively enhance macrophage and monocyte development (Wunderlich et al., 2010; Rios et al., 2019). NSG-QUAD mice combine all four transgenes—IL-3, GM-CSF, SCF, and CSF1—creating a host environment that simultaneously supports multiple branches of human myelopoiesis. Whether this enhanced myeloid compartment translates into functional innate immune competence in vivo has not been systematically established.

Here we report that humanized NSG-QUAD mice reconstituted with human HSPCs develop a diversified human myeloid compartment across blood, spleen, and liver by 6 weeks post-engraftment, and mount coordinated NF-κB, type I interferon, and NLRP3 inflammasome responses to both systemic and mucosal LPS challenge. Using the selective NLRP3 inhibitor MCC950, we demonstrate pathway-specific pharmacological modulation, establishing humanized NSG-QUAD mice as a tractable in vivo platform for studying human innate immunity and evaluating therapeutics targeting these pathways.\
"""

