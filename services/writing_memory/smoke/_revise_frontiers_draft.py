for sec in revised_sections:
    w  = wc(sec["text"])
    ex = expert.get(sec["key"], 0)
    pct = round(w / ex * 100) if ex else 0
    print(f"  {sec['key']:14s} {w:5d} words  (expert {ex}, {pct}%)")
total = sum(wc(s["text"]) for s in revised_sections)
print(f"  {'TOTAL':14s} {total:5d} words  (expert 7351, {round(total/7351*100)}%)")
