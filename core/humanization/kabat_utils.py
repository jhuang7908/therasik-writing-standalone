"""
kabat_utils.py — InSynBio AbEngineCore
=======================================
Canonical Kabat numbering utilities with CORRECT insertion-code handling.

BUG HISTORY (never repeat):
    Previous code stored Kabat positions as plain integers:
        d[pos] = aa          # WRONG — (52,"A") overwrites (52,"")→Y is lost

    This silently drops all insertion-code residues:
        Kabat 52A (CDR-H2)  → lost → CDR-H2 one residue shorter
        Kabat 82A/82B/82C (FR3) → lost → FR3 loop truncated
        Kabat 30A-F (CDR-L1)    → lost → long L1 loops truncated

Correct approach (this module):
    Keys are (int_pos, ins_code) tuples: (52,""), (52,"A"), (82,"A") …
    Sort order preserves Kabat canonical order: integer first, then "" < "A" < "B" …
    CDR range check uses integer part only (as per Kabat convention).
"""

from __future__ import annotations
from typing import Dict, List, Optional, Tuple

# --------------------------------------------------------------------------- #
# Type alias
# --------------------------------------------------------------------------- #
# KabatKey = (integer_position, insertion_code_string)
# insertion_code_string is "" for non-insertion residues, "A"/"B"/… for insertions
KabatKey  = Tuple[int, str]
KabatDict = Dict[KabatKey, str]      # {(52,"A"): "P", (52,""): "Y", …}

# --------------------------------------------------------------------------- #
# Kabat ↔ IMGT position correspondence for Vernier zone residues
# --------------------------------------------------------------------------- #
# Generated by ANARCII dual-scheme run on:
#   VH: IGHV1-69*01 (most common therapeutic VH, 72% clinical frequency Tier-1)
#   VL: IGKV1-39*01 (most common therapeutic VK, 74% clinical frequency Tier-1)
# Method: number same sequence in Kabat + IMGT → align by sequence index.
#
# IMPORTANT SCHEME DIFFERENCES:
#   Kabat VH 27-30 → IMGT 28-35  : Kabat calls these FR1; IMGT calls them CDR-H1
#   Kabat VH 93-94 → IMGT 105/117: Kabat calls these FR3; IMGT calls them CDR-H3
#   All other VH Vernier positions: IMGT number is higher due to IMGT's wider CDRs
#
# The offset is NOT uniform — exact mapping depends on V-gene family and CDR lengths.
# Use kabat_to_imgt_vernier() for programmatic lookup; for other genes use
# get_dual_numbering() which re-runs ANARCII in both schemes.

