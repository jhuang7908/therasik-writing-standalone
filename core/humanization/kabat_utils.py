def sort_key(k: KabatKey) -> Tuple[int, str]:
    """Kabat canonical sort: integer ascending, then insertion code "" < "A" < "B" …"""
    pos, ins = k
    # map "" → comes before "A" in ASCII, so plain string sort works
    return (pos, ins)


def is_in_cdr(pos: int, chain: str) -> bool:
    """Return True if integer Kabat position is within any CDR of the given chain."""
    ranges = CDR_RANGES_VH if chain == "VH" else CDR_RANGES_VL
    return any(lo <= pos <= hi for lo, hi in ranges)


def kabat_from_anarcii(anarcii_numbering: List[Tuple[Tuple[int, str], str]]) -> KabatDict:
    """
    Convert Anarcii `.numbering` list to a KabatDict.

    CORRECT key format: (int_pos, ins_str)
    Drops only gap characters ("-") and None residues.

    NORMALIZATION RULE (must not change):
        ANARCII encodes non-insertion positions as (pos, ' ') — a space character,
        NOT (pos, '').  We normalize all whitespace-only insertion codes to ""
        so that callers can safely use (pos, "") as the key for base positions.

        Examples:
            (52, ' ')  → (52, '')   ← base position Y
            (52, 'A')  → (52, 'A')  ← insertion position P
            (82, 'B')  → (82, 'B')  ← insertion position

    Args:
        anarcii_numbering:  entry["numbering"] from Anarcii output after
                            `engine.to_scheme("kabat")`

    Returns:
        KabatDict — {(pos, ins_normalized): aa}
    """
    d: KabatDict = {}
    for (pos, ins), aa in anarcii_numbering:
        if aa and aa != "-":
            ins_str = ins.strip() if ins else ""   # ' ' → '', 'A' → 'A'
            d[(pos, ins_str)] = aa
    return d


def sorted_keys(kd: KabatDict) -> List[KabatKey]:
    """Return keys in canonical Kabat order."""
    return sorted(kd.keys(), key=sort_key)


def cdr_span(kd: KabatDict, lo: int, hi: int) -> str:
    """
    Extract CDR sequence from KabatDict for Kabat range [lo, hi].
    Includes insertion-code residues (e.g., 52A) within the integer range.
    """
    residues = [
        kd[k] for k in sorted_keys(kd)
        if lo <= k[0] <= hi
    ]
    return "".join(r for r in residues if r)


def assemble_humanized_v(
    chain: str,
    mouse_num:  KabatDict,
    germ_num:   KabatDict,
    bm_map:     Dict[int, str],   # {kabat_int_pos: mouse_aa}  ← back-mutations
    fr4:        str,
) -> str:
    """
    Assemble a humanized variable-domain sequence.

    Rules (per Kabat position):
      1. If position (integer part) is in a CDR range → use mouse residue
      2. If integer position is in bm_map             → use back-mutated (mouse) residue
      3. Otherwise                                     → use germline residue

    Insertion-code positions follow the same rules based on their integer part.

    The V-region ends at:
      VH: Kabat 102 (CDR-H3 end, before FR4 starts at ~103)
      VL: Kabat 97  (CDR-L3 end, before FR4 starts at ~98)

    FR4 is appended directly as a raw string (J-gene sequence).
    """
    cdr_ranges = CDR_RANGES_VH if chain == "VH" else CDR_RANGES_VL
    max_v_end  = MAX_CDR3_VH   if chain == "VH" else MAX_CDR3_VL

    def _src(pos: int, ins: str) -> Optional[str]:
        """Return amino acid for a given (pos, ins) according to assembly rules."""
        in_cdr = any(lo <= pos <= hi for lo, hi in cdr_ranges)
        if in_cdr:
            # STRICT CDR GRAFTING: Only use residues present in the donor (mouse/chim)
            # Do NOT fallback to base position if insertion code is missing in donor,
            # and do NOT use germline residues for CDR positions.
            return mouse_num.get((pos, ins))
        elif pos in bm_map:
            return bm_map[pos]
        else:
            # FRAMEWORK: Use germline residues
            return germ_num.get((pos, ins)) or germ_num.get((pos, ""))

    # Build the keys for the assembled sequence
    # 1. For CDRs: Use ONLY keys present in mouse_num within CDR ranges
    # 2. For FRs: Use keys from germ_num (and bm_map)
    all_keys: set[KabatKey] = set()
    
    # Add all germline keys (Framework)
    for k in germ_num:
        if k[0] <= max_v_end and not is_in_cdr(k[0], chain):
            all_keys.add(k)
            
    # Add all donor keys (CDRs)
    for k in mouse_num:
        if k[0] <= max_v_end and is_in_cdr(k[0], chain):
            all_keys.add(k)
            
    # Add back-mutation keys (might be in FR but not in germ_num if insertion)
    for pos in bm_map:
        if pos <= max_v_end:
            all_keys.add((pos, ""))

    seq_parts: List[str] = []
    for k in sorted(all_keys, key=sort_key):
        pos, ins = k
        aa = _src(pos, ins)
        if aa:
            seq_parts.append(aa)

    return "".join(seq_parts) + fr4


def verify_cdr_preservation(
    humanized_num: KabatDict,
    mouse_num:     KabatDict,
    chain:         str,
) -> List[str]:
    """
    Verify all CDR positions match between humanized and mouse sequences.
    Returns list of mismatch descriptions (empty = all good).
    """
    cdr_ranges = CDR_RANGES_VH if chain == "VH" else CDR_RANGES_VL
    cdr_names  = (["H1","H2","H3"] if chain == "VH" else ["L1","L2","L3"])

    errors: List[str] = []
    for name, (lo, hi) in zip(cdr_names, cdr_ranges):
        m_cdr = cdr_span(mouse_num, lo, hi)
        h_cdr = cdr_span(humanized_num, lo, hi)
        if m_cdr != h_cdr:
            errors.append(
                f"{chain} {name} (Kabat {lo}-{hi}): "
                f"Mouse={m_cdr!r} ≠ Humanized={h_cdr!r}"
            )
    return errors


def self_check_humanization(
    chain: str,
    donor_seq: str,
    humanized_seq: str,
    donor_num: KabatDict,
    humanized_num: KabatDict
) -> Dict[str, Any]:
    """
    Perform automatic self-check of humanization/caninization results.
    Checks: Total length, CDR lengths, FR lengths, and CDR sequence identity.
    """
    cdr_ranges = CDR_RANGES_VH if chain == "VH" else CDR_RANGES_VL
    cdr_names = ["CDR1", "CDR2", "CDR3"]
    
    results = {
        "pass": True,
        "errors": [],
        "metrics": {}
    }
    
    # 1. Total Length Check
    if len(donor_seq) != len(humanized_seq):
        results["pass"] = False
        results["errors"].append(f"Total length mismatch: Donor={len(donor_seq)}, Humanized={len(humanized_seq)}")
    
    # 2. CDR Checks (Length and Sequence)
    for name, (lo, hi) in zip(cdr_names, cdr_ranges):
        d_cdr = cdr_span(donor_num, lo, hi)
        h_cdr = cdr_span(humanized_num, lo, hi)
        
        if len(d_cdr) != len(h_cdr):
            results["pass"] = False
            results["errors"].append(f"{name} length mismatch: Donor={len(d_cdr)}, Humanized={len(h_cdr)}")
        
        if d_cdr != h_cdr:
            results["pass"] = False
            results["errors"].append(f"{name} sequence mismatch: Donor={d_cdr}, Humanized={h_cdr}")
            
        results["metrics"][f"{name}_len"] = len(h_cdr)

    # 3. FR Checks (Length)
    # FR1: start to CDR1_start
    # FR2: CDR1_end to CDR2_start
    # FR3: CDR2_end to CDR3_start
    # FR4: CDR3_end to end
    
    fr_ranges = []
    if chain == "VH":
        fr_ranges = [(1, 25), (36, 49), (66, 94), (103, 113)] # Approximate Kabat
    else:
        fr_ranges = [(1, 23), (35, 49), (57, 88), (98, 107)]
        
    for i, (lo, hi) in enumerate(fr_ranges):
        name = f"FR{i+1}"
        d_fr = cdr_span(donor_num, lo, hi)
        h_fr = cdr_span(humanized_num, lo, hi)
        
        if len(d_fr) != len(h_fr):
            # We record FR length changes but sometimes they are expected if scaffold differs
            # However, per user request "FR", we flag it.
            results["pass"] = False
            results["errors"].append(f"{name} length mismatch: Donor={len(d_fr)}, Humanized={len(h_fr)}")
        
        results["metrics"][f"{name}_len"] = len(h_fr)

    return results


def get_kabat_numbering(seq: str, anarcii_engine=None) -> KabatDict:
    """
    Convenience: run Anarcii on a sequence and return a KabatDict.
    If anarcii_engine is None, creates a new Anarcii instance.
    """
    if anarcii_engine is None:
        from anarcii import Anarcii
        anarcii_engine = Anarcii()

    result = anarcii_engine.number([("seq", seq)])
    try:
        result = anarcii_engine.to_scheme("kabat")
    except Exception:
        pass

    entry = result.get("seq", {})
    if not entry or not entry.get("numbering"):
        return {}

    return kabat_from_anarcii(entry["numbering"])
