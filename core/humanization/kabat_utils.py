def kabat_to_imgt(kabat_pos: int, chain: str) -> Optional[int]:
    """Return IMGT position for a Kabat Vernier zone position, or None if not in table."""
    if chain == "VH":
        return VERNIER_KABAT_TO_IMGT_VH.get(kabat_pos)
    return VERNIER_KABAT_TO_IMGT_VL.get(kabat_pos)


def imgt_to_kabat(imgt_pos: int, chain: str) -> Optional[int]:
    """Return Kabat position for an IMGT Vernier zone position, or None if not in table."""
    if chain == "VH":
        return VERNIER_IMGT_TO_KABAT_VH.get(imgt_pos)
    return VERNIER_IMGT_TO_KABAT_VL.get(imgt_pos)


def vernier_dual_numbering_table(chain: str) -> list[dict]:
    """
    Return the full Vernier zone dual-numbering table as a list of dicts for reporting.

    Each dict has keys: kabat, imgt, aa_ref, kabat_region, imgt_region.
    aa_ref is from the reference germline (IGHV1-69 / IGKV1-39).
    """
    k2i = VERNIER_KABAT_TO_IMGT_VH if chain == "VH" else VERNIER_KABAT_TO_IMGT_VL
    kr  = _VH_KABAT_REGION         if chain == "VH" else _VL_KABAT_REGION
    ir  = _VH_IMGT_REGION          if chain == "VH" else _VL_IMGT_REGION
    rows = []
    for kpos, ipos in sorted(k2i.items()):
        rows.append({
            "chain":        chain,
            "kabat":        kpos,
            "imgt":         ipos,
            "kabat_region": kr.get(kpos, "?"),
            "imgt_region":  ir.get(ipos, "?"),
            "note": (
                "Kabat=FR1, IMGT=CDR-H1 (scheme boundary differs)" if chain == "VH" and kpos in (27,28,29,30)
                else "Kabat=FR3, IMGT=CDR-H3 (scheme boundary differs)" if chain == "VH" and kpos in (93,94)
                else ""
            ),
        })
    return rows


# --------------------------------------------------------------------------- #
# Canonical CDR ranges — V5.1.0 Union (Kabat ∪ Chothia ∪ AbM)
# --------------------------------------------------------------------------- #
# SSOT: config/vh_vl_humanization_v490.json `_meta.version: 5.1.0`,
#       `cdr_definitions.regions` (VH H1=26-35, H2=50-65, H3=95-102;
#                                   VL L1=24-34, L2=50-56, L3=89-97).
# These three lists MUST match `_CDR_POS` in core/humanization/engine.py
# (`_phase4_real`) and `cdr_definitions.regions` in the SSOT JSON.
# Trigger for the V5.1.0 widening: R5-307 / Wemol H4-L4 cross-comparison
# (2026-05-01) — strict Chothia 26-32/52-56 silently humanized
# paratope-adjacent residues 33-35, 50-51, 57-65. See EVOLUTION_LOG 2026-05-01.
CDR_RANGES_VH: List[Tuple[int, int]] = [(26, 35), (50, 65), (95, 102)]
CDR_RANGES_VL: List[Tuple[int, int]] = [(24, 34), (50, 56), (89,  97)]

# FR4 is NOT part of the V-gene; assembly appends it separately.
MAX_V_POS_VH = 94   # last FR3 integer position before CDR-H3 (95+)
MAX_CDR3_VH  = 102  # Kabat CDR-H3 end
MAX_V_POS_VL = 88   # last FR3 integer position before CDR-L3 (89+)
MAX_CDR3_VL  = 97   # Kabat CDR-L3 end

